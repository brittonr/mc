fn assert_report_has_code(report: &GuardReport, code: &str) -> Result<(), String> {
    if report
        .diagnostics
        .iter()
        .any(|diagnostic| diagnostic.code == code)
    {
        Ok(())
    } else {
        Err(format!(
            "expected diagnostic code {code}; got {:?}",
            report.diagnostics
        ))
    }
}

fn run_self_test() -> Result<(), String> {
    let rules = default_rules();
    let valid = fixture_valid_model();
    let valid_report = validate_layout(&valid, &rules);
    if !valid_report.diagnostics.is_empty() {
        return Err(format!(
            "positive fixture unexpectedly failed: {:?}",
            valid_report.diagnostics
        ));
    }

    let mut undocumented_root = valid.clone();
    undocumented_root.tree.push(tree_dir("scratch-component"));
    let undocumented_root_report = validate_layout(&undocumented_root, &rules);
    assert_report_has_code(&undocumented_root_report, "undocumented_root_directory")?;
    if !report_has_required_findings(&undocumented_root_report) {
        return Err(String::from("undocumented root fixture should be required"));
    }

    let mut surprise_git = valid.clone();
    surprise_git.tree.push(tree_dir("vendor"));
    surprise_git.tree.push(tree_dir("vendor/.git"));
    let surprise_git_report = validate_layout(&surprise_git, &rules);
    assert_report_has_code(&surprise_git_report, "undocumented_nested_git")?;

    let mut transient_root = valid.clone();
    transient_root.tree.push(TreeEntry {
        path: String::from("target-mc-compat-live.log"),
        kind: EntryKind::File,
    });
    let transient_report = validate_layout(&transient_root, &rules);
    assert_report_has_code(&transient_report, "root_transient_artifact")?;
    if report_has_required_findings(&transient_report) {
        return Err(String::from("transient root artifact should be advisory"));
    }

    let mut missing_docs = valid.clone();
    missing_docs
        .tree
        .retain(|entry| entry.path != STEVENARELLA_AGENTS_PATH);
    let missing_docs_report = validate_layout(&missing_docs, &rules);
    assert_report_has_code(&missing_docs_report, "missing_subtree_documentation")?;

    let mut generated_drift = valid.clone();
    generated_drift.generated_text.insert(
        GENERATED_RUST_PATH.to_string(),
        String::from("// hand edited generated table"),
    );
    let generated_drift_report = validate_layout(&generated_drift, &rules);
    assert_report_has_code(&generated_drift_report, "generated_marker_drift")?;

    let mut registry_mismatch = valid.clone();
    registry_mismatch
        .tree
        .push(tree_dir("clients/stevenarella/.git"));
    let registry_mismatch_report = validate_layout(&registry_mismatch, &rules);
    assert_report_has_code(&registry_mismatch_report, "component_registry_mismatch")?;

    let mut missing_contract = valid;
    missing_contract.docs.checklist = String::from("| Path | Role | Ownership | Local notes | Default gates |\n| --- | --- | --- | --- | --- |\n");
    let missing_contract_report = validate_layout(&missing_contract, &rules);
    assert_report_has_code(&missing_contract_report, "missing_layout_guard_contract")?;

    Ok(())
}

#[derive(Debug, Clone)]
struct Command {
    root: PathBuf,
    self_test: bool,
}

fn parse_args() -> Result<Command, String> {
    let mut args = env::args().skip(1);
    let mut root = PathBuf::from(DEFAULT_ROOT);
    let mut self_test = false;

    while let Some(arg) = args.next() {
        if arg == ROOT_FLAG {
            let value = args
                .next()
                .ok_or_else(|| format!("{ROOT_FLAG} requires a path"))?;
            root = PathBuf::from(value);
        } else if arg == SELF_TEST_FLAG {
            self_test = true;
        } else {
            return Err(format!("unknown argument: {arg}"));
        }
    }

    Ok(Command { root, self_test })
}

fn run(command: Command) -> Result<String, String> {
    if command.self_test {
        run_self_test()?;
        return Ok(String::from(SELF_TEST_SUCCESS_MESSAGE));
    }

    let rules = default_rules();
    let model = load_repository_model(&command.root, &rules)?;
    let report = validate_layout(&model, &rules);
    if report_has_required_findings(&report) {
        Err(render_diagnostics(&report))
    } else {
        Ok(render_success(&report))
    }
}

fn main() -> ExitCode {
    let command = match parse_args() {
        Ok(command) => command,
        Err(error) => {
            eprintln!("{error}");
            return FAILURE;
        }
    };

    match run(command) {
        Ok(message) => {
            println!("{message}");
            SUCCESS
        }
        Err(error) => {
            eprintln!("{error}");
            FAILURE
        }
    }
}
