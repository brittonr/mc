fn validate_layout(model: &RepositoryModel, rules: &LayoutRules) -> GuardReport {
    let registry = parse_component_registry(&model.docs.checklist);
    let mut diagnostics = Vec::new();

    diagnostics.extend(check_guard_contract(&model.docs.checklist));
    diagnostics.extend(check_legacy_layout_docs(&model.docs, &model.tree));
    diagnostics.extend(check_component_registry(&model.tree, &registry));
    diagnostics.extend(check_root_entries(&model.tree, &registry));
    diagnostics.extend(check_nested_git(&model.tree, &registry));
    diagnostics.extend(check_generated_surfaces(&model.generated_text, rules));

    diagnostics.sort_by(|left, right| {
        left.severity
            .cmp(&right.severity)
            .then_with(|| left.code.cmp(right.code))
            .then_with(|| left.path.cmp(&right.path))
    });

    GuardReport { diagnostics }
}

fn check_guard_contract(checklist: &str) -> Vec<Diagnostic> {
    let missing = missing_items(checklist, REQUIRED_CONTRACT_PHRASES);
    if missing.is_empty() {
        Vec::new()
    } else {
        vec![diagnostic(
            "missing_layout_guard_contract",
            REQUIRED,
            CHECKLIST_PATH,
            "layout_guard_contract",
            format!(
                "{} is missing guard contract terms: {}",
                CHECKLIST_PATH,
                missing.join(", ")
            ),
            "docs/layout-checklist.md owns the layout guard contract",
            "document diagnostic classes, waiver model, source-of-truth inputs, and non-claims",
        )]
    }
}

