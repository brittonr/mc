fn parse_component_registry(checklist: &str) -> Vec<RegistryEntry> {
    let mut entries = Vec::new();
    for line in checklist.lines() {
        let trimmed = line.trim();
        if !trimmed.starts_with('|') || trimmed.contains("---") || trimmed.contains("Path | Role") {
            continue;
        }

        let cells = trimmed
            .trim_matches('|')
            .split('|')
            .map(|cell| cell.trim().to_string())
            .collect::<Vec<_>>();
        if cells.len() < REGISTRY_EXPECTED_CELL_COUNT {
            continue;
        }

        let Some(path) = extract_first_backticked_path(&cells[REGISTRY_PATH_CELL_INDEX]) else {
            continue;
        };

        entries.push(RegistryEntry {
            path,
            role: cells[REGISTRY_ROLE_CELL_INDEX].clone(),
            ownership: cells[REGISTRY_OWNERSHIP_CELL_INDEX].clone(),
            local_notes: cells[REGISTRY_LOCAL_NOTES_CELL_INDEX].clone(),
            default_gates: cells[REGISTRY_DEFAULT_GATES_CELL_INDEX].clone(),
        });
    }
    entries
}

fn extract_first_backticked_path(text: &str) -> Option<String> {
    let (_, after_open) = text.split_once('`')?;
    let (path, _) = after_open.split_once('`')?;
    if path.ends_with(ROOT_PATH_SUFFIX) {
        Some(path.to_string())
    } else {
        None
    }
}

fn extract_backticked_paths(text: &str) -> Vec<String> {
    let mut paths = Vec::new();
    let mut rest = text;
    while let Some((_, after_open)) = rest.split_once('`') {
        let Some((candidate, after_close)) = after_open.split_once('`') else {
            break;
        };
        if is_relative_doc_path(candidate) {
            paths.push(candidate.to_string());
        }
        rest = after_close;
    }
    paths
}

fn is_relative_doc_path(path: &str) -> bool {
    !path.is_empty()
        && !path.starts_with(PATH_SEPARATOR)
        && !path.ends_with(ROOT_PATH_SUFFIX)
        && !path.contains("..")
}

fn check_component_registry(tree: &[TreeEntry], registry: &[RegistryEntry]) -> Vec<Diagnostic> {
    let mut diagnostics = Vec::new();
    for entry in registry {
        let component_observed = tree_contains_path(tree, trim_root_suffix(&entry.path));
        let parent_owned = is_parent_owned(entry);
        if parent_owned && !component_observed {
            diagnostics.push(diagnostic(
                "component_registry_mismatch",
                REQUIRED,
                entry.path.clone(),
                "component_registry_mismatch",
                format!("parent-owned registry path {} is missing from the modeled tree", entry.path),
                "component registry rows in docs/layout-checklist.md are authoritative for parent-owned roots",
                "restore the component root or change the registry row with a reviewable waiver",
            ));
            continue;
        }

        if parent_owned
            && tree_contains_path(
                tree,
                &format!("{}{}", trim_root_suffix(&entry.path), GIT_BOUNDARY_SUFFIX),
            )
        {
            diagnostics.push(diagnostic(
                "component_registry_mismatch",
                REQUIRED,
                entry.path.clone(),
                "component_registry_mismatch",
                format!("parent-owned registry path {} contains a nested Git boundary", entry.path),
                "parent-owned core component trees use the parent repository status",
                "remove the nested checkout or reclassify the registry row before treating it as parent-owned",
            ));
        }

        if should_check_local_notes(entry, component_observed) {
            for note_path in extract_backticked_paths(&entry.local_notes) {
                if !tree_contains_path(tree, &note_path) {
                    diagnostics.push(diagnostic(
                        "missing_subtree_documentation",
                        REQUIRED,
                        note_path.clone(),
                        "missing_subtree_documentation",
                        format!("registry path {} lists missing local note {}", entry.path, note_path),
                        "local-note paths listed in docs/layout-checklist.md are guard inputs",
                        "add the local note file or replace the local-note cell with an explicit waiver",
                    ));
                }
            }
        }
    }
    diagnostics
}

fn should_check_local_notes(entry: &RegistryEntry, component_observed: bool) -> bool {
    (component_observed || is_parent_owned(entry)) && !has_local_note_waiver(entry)
}

fn is_parent_owned(entry: &RegistryEntry) -> bool {
    entry.ownership.contains("Parent repository owned")
}

fn has_local_note_waiver(entry: &RegistryEntry) -> bool {
    let combined =
        format!("{} {} {}", entry.role, entry.ownership, entry.local_notes).to_ascii_lowercase();
    combined.contains("waived") || combined.contains("waiver")
}

fn check_root_entries(tree: &[TreeEntry], registry: &[RegistryEntry]) -> Vec<Diagnostic> {
    let documented_roots = documented_root_names(registry);
    let mut diagnostics = Vec::new();

    for entry in root_entries(tree) {
        if is_allowed_root_file(&entry.path) || is_hidden_root_entry(&entry.path) {
            continue;
        }
        if is_transient_root_artifact(&entry.path) {
            diagnostics.push(diagnostic(
                "root_transient_artifact",
                ADVISORY,
                entry.path.clone(),
                "root_transient_artifact",
                format!("root transient artifact {} should stay scratch-only", entry.path),
                "transient root artifacts are advisory unless cited as review evidence",
                "move review-critical output under docs/evidence/ with a BLAKE3 manifest, otherwise leave it ignored or clean it locally",
            ));
            continue;
        }
        if entry.kind == EntryKind::Directory && !documented_roots.contains(&entry.path) {
            diagnostics.push(diagnostic(
                "undocumented_root_directory",
                REQUIRED,
                entry.path.clone(),
                "undocumented_root_directory",
                format!("root directory {} is absent from docs/layout-checklist.md", entry.path),
                "docs/layout-checklist.md is the component registry and root allowlist",
                "document the root directory with ownership/default-gate policy or remove the accidental directory",
            ));
        }
    }

    diagnostics
}

fn root_entries(tree: &[TreeEntry]) -> Vec<&TreeEntry> {
    tree.iter()
        .filter(|entry| !entry.path.contains(PATH_SEPARATOR))
        .collect()
}

fn documented_root_names(registry: &[RegistryEntry]) -> BTreeSet<String> {
    registry
        .iter()
        .filter_map(|entry| first_path_segment(&entry.path))
        .collect()
}

fn first_path_segment(path: &str) -> Option<String> {
    path.splitn(FIRST_PATH_SEGMENT_LIMIT, PATH_SEPARATOR)
        .next()
        .filter(|segment| !segment.is_empty())
        .map(ToString::to_string)
}

fn is_allowed_root_file(path: &str) -> bool {
    ALLOWED_ROOT_FILES.iter().any(|allowed| path == *allowed)
}

fn is_hidden_root_entry(path: &str) -> bool {
    path.starts_with('.')
}

fn is_transient_root_artifact(path: &str) -> bool {
    path == RESULT_LINK_NAME
        || path.starts_with(RESULT_LINK_PREFIX)
        || path == TARGET_DIR_NAME
        || (path.starts_with(TARGET_LOG_PREFIX) && path.ends_with(LOG_SUFFIX))
        || path.ends_with(RUN_LOG_SUFFIX)
}

fn check_nested_git(tree: &[TreeEntry], registry: &[RegistryEntry]) -> Vec<Diagnostic> {
    let allowed = allowed_nested_git_roots(registry);
    let mut diagnostics = Vec::new();

    for entry in tree.iter().filter(|entry| is_git_boundary(&entry.path)) {
        let Some(owner_root) = git_boundary_owner_root(&entry.path) else {
            continue;
        };
        if !allowed.contains(&owner_root) {
            diagnostics.push(diagnostic(
                "undocumented_nested_git",
                REQUIRED,
                owner_root.clone(),
                "undocumented_nested_git",
                format!("nested Git boundary {} is not a documented exception", entry.path),
                "nested Git exceptions must be listed in docs/layout-checklist.md or the component registry",
                "classify the checkout as parent-owned, reference-only, or external before relying on it",
            ));
        }
    }

    diagnostics
}

fn allowed_nested_git_roots(registry: &[RegistryEntry]) -> BTreeSet<String> {
    registry
        .iter()
        .filter(|entry| expects_nested_git(entry))
        .map(|entry| trim_root_suffix(&entry.path).to_string())
        .collect()
}

fn expects_nested_git(entry: &RegistryEntry) -> bool {
    let combined = format!("{} {} {}", entry.role, entry.ownership, entry.local_notes);
    combined.contains("Nested repo")
        || combined.contains("nested Git")
        || combined.contains("nested checkout")
        || combined.contains("separate jj/git workflow")
}

fn is_git_boundary(path: &str) -> bool {
    path == GIT_ENTRY_NAME || path.ends_with(GIT_BOUNDARY_SUFFIX)
}

fn git_boundary_owner_root(path: &str) -> Option<String> {
    path.strip_suffix(GIT_BOUNDARY_SUFFIX)
        .map(ToString::to_string)
}

fn check_generated_surfaces(
    generated_text: &BTreeMap<String, String>,
    rules: &LayoutRules,
) -> Vec<Diagnostic> {
    let mut diagnostics = Vec::new();
    for surface in &rules.generated_surfaces {
        match generated_text.get(surface.path) {
            Some(text) if text.contains(surface.marker) => {}
            Some(_) => diagnostics.push(diagnostic(
                "generated_marker_drift",
                REQUIRED,
                surface.path,
                "generated_marker_drift",
                format!("generated surface {} is missing marker {:?}", surface.path, surface.marker),
                surface.owner_hint,
                "refresh generated surfaces or restore the ownership marker before editing by hand",
            )),
            None => diagnostics.push(diagnostic(
                "generated_marker_drift",
                REQUIRED,
                surface.path,
                "generated_marker_drift",
                format!("generated surface {} is missing from the modeled tree", surface.path),
                surface.owner_hint,
                "restore the generated surface or update the guard rule with a reviewed source-of-truth change",
            )),
        }
    }
    diagnostics
}

fn tree_contains_path(tree: &[TreeEntry], path: &str) -> bool {
    tree.iter().any(|entry| entry.path == path)
}

fn trim_root_suffix(path: &str) -> &str {
    path.trim_end_matches(ROOT_PATH_SUFFIX)
}

fn report_has_required_findings(report: &GuardReport) -> bool {
    report
        .diagnostics
        .iter()
        .any(|diagnostic| diagnostic.severity == REQUIRED)
}

fn render_success(report: &GuardReport) -> String {
    let advisory_count = report
        .diagnostics
        .iter()
        .filter(|diagnostic| diagnostic.severity == ADVISORY)
        .count();
    if advisory_count == 0 {
        String::from(SUCCESS_MESSAGE)
    } else {
        format!(
            "{SUCCESS_MESSAGE}; advisory_findings={advisory_count}\n{}",
            render_diagnostics(report)
        )
    }
}

fn render_diagnostics(report: &GuardReport) -> String {
    report
        .diagnostics
        .iter()
        .map(render_diagnostic)
        .collect::<Vec<_>>()
        .join("\n")
}

fn render_diagnostic(diagnostic: &Diagnostic) -> String {
    format!(
        "severity={:?} code={} path={} rule={} message={} owner_or_waiver_hint={} next_action={}",
        diagnostic.severity,
        diagnostic.code,
        diagnostic.path,
        diagnostic.rule,
        diagnostic.message,
        diagnostic.owner_or_waiver_hint,
        diagnostic.next_action
    )
}

fn read_to_string(root: &Path, relative: &str) -> Result<String, String> {
    let path = root.join(relative);
    fs::read_to_string(&path).map_err(|error| format!("failed to read {}: {error}", path.display()))
}

fn load_repository_model(root: &Path, rules: &LayoutRules) -> Result<RepositoryModel, String> {
    let docs = LayoutDocs {
        agents: read_to_string(root, AGENTS_PATH)?,
        readme: read_to_string(root, README_PATH)?,
        architecture: read_to_string(root, ARCHITECTURE_PATH)?,
        checklist: read_to_string(root, CHECKLIST_PATH)?,
    };
    let mut tree = Vec::new();
    collect_tree_entries(root, root, &mut tree)?;
    tree.sort_by(|left, right| left.path.cmp(&right.path));

    let mut generated_text = BTreeMap::new();
    for surface in &rules.generated_surfaces {
        if tree_contains_path(&tree, surface.path) {
            generated_text.insert(
                surface.path.to_string(),
                read_to_string(root, surface.path)?,
            );
        }
    }

    Ok(RepositoryModel {
        docs,
        tree,
        generated_text,
    })
}

fn collect_tree_entries(
    root: &Path,
    current: &Path,
    tree: &mut Vec<TreeEntry>,
) -> Result<(), String> {
    let entries = fs::read_dir(current)
        .map_err(|error| format!("failed to read directory {}: {error}", current.display()))?;
    for entry_result in entries {
        let entry = entry_result.map_err(|error| {
            format!(
                "failed to read directory entry {}: {error}",
                current.display()
            )
        })?;
        let path = entry.path();
        let metadata = fs::symlink_metadata(&path)
            .map_err(|error| format!("failed to inspect {}: {error}", path.display()))?;
        let relative = relative_path(root, &path)?;
        let kind = if metadata.file_type().is_symlink() {
            EntryKind::Symlink
        } else if metadata.is_dir() {
            EntryKind::Directory
        } else {
            EntryKind::File
        };
        tree.push(TreeEntry {
            path: relative.clone(),
            kind,
        });

        if metadata.is_dir() && !should_skip_walk(&relative) {
            collect_tree_entries(root, &path, tree)?;
        }
    }
    Ok(())
}

fn should_skip_walk(relative: &str) -> bool {
    if is_git_boundary(relative)
        || relative == TARGET_DIR_NAME
        || relative == RESULT_LINK_NAME
        || relative.starts_with(RESULT_LINK_PREFIX)
    {
        return true;
    }
    IGNORED_WALK_DIRS.iter().any(|ignored| {
        relative == *ignored || relative.starts_with(&format!("{ignored}{PATH_SEPARATOR}"))
    })
}

fn relative_path(root: &Path, path: &Path) -> Result<String, String> {
    path.strip_prefix(root)
        .map_err(|error| format!("failed to relativize {}: {error}", path.display()))?
        .to_str()
        .map(|text| text.replace('\\', "/"))
        .ok_or_else(|| format!("{} is not valid UTF-8", path.display()))
}

