#[derive(Debug, Clone, PartialEq, Eq)]
struct Diagnostic {
    code: &'static str,
    severity: Severity,
    path: String,
    rule: &'static str,
    message: String,
    owner_or_waiver_hint: String,
    next_action: String,
}

#[derive(Debug, Clone, PartialEq, Eq)]
struct GuardReport {
    diagnostics: Vec<Diagnostic>,
}

fn contains_all(text: &str, needles: &[&str]) -> bool {
    needles.iter().all(|needle| text.contains(needle))
}

fn missing_items<'a>(text: &str, items: &'a [&str]) -> Vec<&'a str> {
    items
        .iter()
        .copied()
        .filter(|item| !text.contains(item))
        .collect()
}

fn push_missing_doc(
    diagnostics: &mut Vec<Diagnostic>,
    code: &'static str,
    path: &str,
    needs: &[&str],
) {
    if needs.is_empty() {
        return;
    }

    diagnostics.push(Diagnostic {
        code,
        severity: REQUIRED,
        path: path.to_string(),
        rule: "missing_subtree_documentation",
        message: format!("{path} must mention: {}", needs.join(", ")),
        owner_or_waiver_hint: String::from("update root layout docs or record an explicit waiver"),
        next_action: String::from("edit AGENTS.md, README.md, docs/architecture.md, or docs/layout-checklist.md so the boundary is reviewable"),
    });
}

fn diagnostic(
    code: &'static str,
    severity: Severity,
    path: impl Into<String>,
    rule: &'static str,
    message: impl Into<String>,
    owner_or_waiver_hint: impl Into<String>,
    next_action: impl Into<String>,
) -> Diagnostic {
    Diagnostic {
        code,
        severity,
        path: path.into(),
        rule,
        message: message.into(),
        owner_or_waiver_hint: owner_or_waiver_hint.into(),
        next_action: next_action.into(),
    }
}

